package sample1;

public class WhileAndDoWhile {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
	
		int i=0;
		while(i<11){
			System.out.println(i);
			i++;
		}
		
		 int count = 0;
	        do {
	            System.out.println("Count is: " + count);
	            count++;
	        } while (count < 11);
	}

}
